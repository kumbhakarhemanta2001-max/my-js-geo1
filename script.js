    function loadMCQ() {
        const container = document.getElementById('mcq-container');
        questions.forEach((item, index) => {
            const card = document.createElement('div');
            card.className = 'mcq-card';
            card.innerHTML = `
                <span class="q-text">${index + 1}. ${item.q}</span>
                <div class="options-grid">
                    ${item.opts.map(opt => `<div class="opt">${opt}</div>`).join('')}
                </div>
                <button class="ans-btn" onclick="toggleAns(${index})">
                    <i class="fas fa-eye"></i> Answer
                </button>
                <div id="ans-${index}" class="ans-content">সঠিক উত্তর: ${item.a}</div>
            `;
            container.appendChild(card);
        });
    }

    function toggleAns(id) {
        const ansDiv = document.getElementById(`ans-${id}`);
        const btn = ansDiv.previousElementSibling;
        if (ansDiv.style.display === "block") {
            ansDiv.style.display = "none";
            btn.innerHTML = '<i class="fas fa-eye"></i> Answer';
        } else {
            ansDiv.style.display = "block";
            btn.innerHTML = '<i class="fas fa-eye-slash"></i> Hide';
        }
    }

    window.onload = loadMCQ;